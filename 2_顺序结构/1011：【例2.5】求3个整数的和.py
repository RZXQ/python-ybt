#
# 【题目描述】
# 按照由小到大的顺序，输出1∼n之间的所有偶数。
#
# 【输入】
# 输入一个整数 n，表示输出范围的上限。
#
# 【输出】
# 输出 1 到 n 之间所有的偶数，每两个偶数之间用空格隔开，结果在同一行。

# 读取用户输入，并将输入的内容转换为整数类型
n = int(input())  # 这一行要求用户输入一个数字（比如 10）

# 初始化变量 i，从第一个偶数 2 开始
i = 2  # 偶数序列是从 2 开始累加的，所以将初始值设为 2

# 创建一个 while 循环，条件是 i 小于等于 n
while i <= n:
    # 打印当前偶数，使用 print 函数的 end 参数确保输出在一行内，用空格分隔每个数字
    print(i, end=' ')  # 例如：输出 '2 ' 不换行，后面的数字(比如 4)会接着打印在同一行

    # 将 i 的值加 2，跳到下一个偶数
    i = i + 2  # 每次循环，i 增加 2，比如从 2 到 4，再到 6，直到超过 n 为止
